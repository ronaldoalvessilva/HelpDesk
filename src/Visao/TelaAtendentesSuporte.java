    private void jBtExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtExcluirActionPerformed
        // TODO add your handling code here:
        buscarAcessoUsuario(telaCadastroAtendentes);
        if (nameUser.equals("ADMINISTRADOR DO SISTEMA") || codigoUser == codUserAcesso && nomeTela.equals(telaCadastroAtendentes) && codExcluir == 1) {
            verificarChamados();
            statusMov = "Excluiu";
            horaMov = jHoraSistema.getText();
            dataModFinal = jDataSistema.getText();
            if (jIdAtendente.getText().equals(codigoAtendente)) {
                JOptionPane.showMessageDialog(rootPane, "Não é possível excluir esse solicitante, existe chamados associado a esse solicitante.");
            } else {
                int resposta = JOptionPane.showConfirmDialog(this, "Deseja realmente excluir registro selecionado?", "Confirmação",
                        JOptionPane.YES_NO_OPTION);
                if (resposta == JOptionPane.YES_OPTION) {
                    objAtende.setIdAtendente(Integer.valueOf(jIdAtendente.getText()));
                    control.excluirAtendente(objAtende);
                    bloquearBotoes();
                    Excluir();
                }
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Usuário não tem acesso ao registro.");
        }
    }//GEN-LAST:event_jBtExcluirActionPerformed

    private void jBtSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtSalvarActionPerformed
        // TODO add your handling code here:
        buscarAcessoUsuario(telaCadastroAtendentes);
        if (nameUser.equals("ADMINISTRADOR DO SISTEMA") || codigoUser == codUserAcesso && nomeTela.equals(telaCadastroAtendentes) && codGravar == 1) {
            if (jNomeAtendente.getText().equals("")) {
                JOptionPane.showMessageDialog(rootPane, "Informe o nome do Atendente.");
            } else if (jComboBoxFuncao.getSelectedItem().equals("")) {
                JOptionPane.showMessageDialog(rootPane, "Informe a função do Atendente.");
            } else if (jComboBoxTipoAtendente.getSelectedItem().equals("")) {
                JOptionPane.showMessageDialog(rootPane, "Informe o Tipo de Atendente.");
            } else {
                objAtende.setStatusAtendente((String) jComboBoxStatusAtendente.getSelectedItem());
                objAtende.setDataCadastro(jDataCadastro.getDate());
                objAtende.setNomeAtendente(jNomeAtendente.getText());
                objAtende.setFuncao((String) jComboBoxFuncao.getSelectedItem());
                objAtende.setTipo((String) jComboBoxTipoAtendente.getSelectedItem());
                objAtende.setObservacaoAtendente(jObservacaoAtendente.getText());
                if (acao == 1) {
                    objAtende.setUsuarioInsert(nameUser);
                    objAtende.setDataInsert(dataModFinal);
                    objAtende.setHorarioInsert(horaMov);
                    control.incluirAtendente(objAtende);
                    buscarID();
                    bloquearBotoes();
                    Salvar();
                    //
                    objLog();
                    controlLog.incluirLogSistema(objLogSys); // Grava o log da operação
                    JOptionPane.showMessageDialog(rootPane, "Registro gravado com sucesso.");
                }
            }
            if (acao == 2) {
                objAtende.setUsuarioUp(nameUser);
                objAtende.setDataUp(dataModFinal);
                objAtende.setHorarioUp(horaMov);
                objAtende.setIdAtendente(Integer.valueOf(jIdAtendente.getText()));
                control.alterarAtendente(objAtende);
                //
                objLog();
                controlLog.incluirLogSistema(objLogSys); // Grava o log da operação
                bloquearBotoes();
                Salvar();
                JOptionPane.showMessageDialog(rootPane, "Registro gravado com sucesso.");
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Usuário não tem acesso ao registro.");
        }
    }//GEN-LAST:event_jBtSalvarActionPerformed

