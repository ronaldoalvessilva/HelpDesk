    private void jTabelaAtendentesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabelaAtendentesMouseClicked
        // TODO add your handling code here:
        flag = 1;
        if (flag == 1) {
            String idAtendente = "" + jTabelaAtendentes.getValueAt(jTabelaAtendentes.getSelectedRow(), 0);
            String nomeAtendente = "" + jTabelaAtendentes.getValueAt(jTabelaAtendentes.getSelectedRow(), 3);
            jCodigo.setText(idAtendente);
            jPesqNomeAtendente.setText(nomeAtendente);
            //
            bloquearCampos();
            bloquearBotoes();
            jBtNovo.setEnabled(true);
            jBtAlterar.setEnabled(true);
            jBtExcluir.setEnabled(true);
            jBtAuditoria.setEnabled(true);
            //
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM ATENDENTES "
                        + "WHERE IdAtendente='" + idAtendente + "'");
                conecta.rs.first();
                jIdAtendente.setText(String.valueOf(conecta.rs.getInt("IdAtendente")));
                jComboBoxStatusAtendente.setSelectedItem(conecta.rs.getString("StatusAtendente"));
                jDataCadastro.setDate(conecta.rs.getDate("DataCadastro"));
                jNomeAtendente.setText(conecta.rs.getString("NomeAtendente"));
                jComboBoxFuncao.setSelectedItem(conecta.rs.getString("Funcao"));
                jComboBoxTipoAtendente.setSelectedItem(conecta.rs.getString("Tipo"));
                jObservacaoAtendente.setText(conecta.rs.getString("ObservacaoAtendente"));
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(rootPane, "ERRO na pesquisa dos dados.\nERROR: " + e);
            }
            conecta.desconecta();
        }
    }//GEN-LAST:event_jTabelaAtendentesMouseClicked

