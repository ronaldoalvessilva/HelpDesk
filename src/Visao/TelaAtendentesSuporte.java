    public void pesquisarAtendentes(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código", "Data", "Status", "Nome do Atendentes"};
        conecta.abrirConexao();
        try {
            conecta.executaSQL(sql);
            conecta.rs.first();
            do {
                count = count + 1;
                // Formatar a data no formato Brasil
                dataBrasil = conecta.rs.getString("DataCadastro");
                String dia = dataBrasil.substring(8, 10);
                String mes = dataBrasil.substring(5, 7);
                String ano = dataBrasil.substring(0, 4);
                dataBrasil = dia + "/" + mes + "/" + ano;
                jtotalRegistros.setText(Integer.toString(count)); // Converter inteiro em string para exibir na tela
                dados.add(new Object[]{conecta.rs.getInt("IdAtendente"), dataBrasil, conecta.rs.getString("StatusAtendente"), conecta.rs.getString("NomeAtendente")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Não existem dados a serem EXIBIDOS !!!");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaAtendentes.setModel(modelo);
        jTabelaAtendentes.getColumnModel().getColumn(0).setPreferredWidth(70);
        jTabelaAtendentes.getColumnModel().getColumn(0).setResizable(false);
        jTabelaAtendentes.getColumnModel().getColumn(1).setPreferredWidth(70);
        jTabelaAtendentes.getColumnModel().getColumn(1).setResizable(false);
        jTabelaAtendentes.getColumnModel().getColumn(2).setPreferredWidth(80);
        jTabelaAtendentes.getColumnModel().getColumn(2).setResizable(false);
        jTabelaAtendentes.getColumnModel().getColumn(3).setPreferredWidth(380);
        jTabelaAtendentes.getColumnModel().getColumn(3).setResizable(false);
        jTabelaAtendentes.getTableHeader().setReorderingAllowed(false);
        jTabelaAtendentes.setAutoResizeMode(jTabelaAtendentes.AUTO_RESIZE_OFF);
        jTabelaAtendentes.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        alinharCamposTabela();
        conecta.desconecta();
    }

