    public void pesquisarEmpresasLaborativas(String sql) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código", "Descrição da Unidade"};
        conecta.abrirConexao();
        conecta.executaSQL(sql);
        try {
            conecta.rs.first();
            do {
                dados.add(new Object[]{conecta.rs.getInt("IdUnidEmp"), conecta.rs.getString("DescricaoUnidade")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Dados não encontrado, use o botão TODOS \nPara pesquisar TODOS OS REGISTROS");
        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaUnidades.setModel(modelo);
        jTabelaUnidades.getColumnModel().getColumn(0).setPreferredWidth(70);
        jTabelaUnidades.getColumnModel().getColumn(0).setResizable(false);
        jTabelaUnidades.getColumnModel().getColumn(1).setPreferredWidth(350);
        jTabelaUnidades.getColumnModel().getColumn(1).setResizable(false);
        jTabelaUnidades.getTableHeader().setReorderingAllowed(false);
        jTabelaUnidades.setAutoResizeMode(jTabelaUnidades.AUTO_RESIZE_OFF);
        jTabelaUnidades.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        alinharCamposTabela();
        conecta.desconecta();
    }

    public void limparTabela() {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código", "Descrição da Unidade"};
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTabelaUnidades.setModel(modelo);
        jTabelaUnidades.getColumnModel().getColumn(0).setPreferredWidth(70);
        jTabelaUnidades.getColumnModel().getColumn(0).setResizable(false);
        jTabelaUnidades.getColumnModel().getColumn(1).setPreferredWidth(350);
        jTabelaUnidades.getColumnModel().getColumn(1).setResizable(false);
        jTabelaUnidades.getTableHeader().setReorderingAllowed(false);
        jTabelaUnidades.setAutoResizeMode(jTabelaUnidades.AUTO_RESIZE_OFF);
        jTabelaUnidades.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        modelo.getLinhas().clear();
    }

    public void alinharCamposTabela() {
        DefaultTableCellRenderer esquerda = new DefaultTableCellRenderer();
        DefaultTableCellRenderer centralizado = new DefaultTableCellRenderer();
        DefaultTableCellRenderer direita = new DefaultTableCellRenderer();
        esquerda.setHorizontalAlignment(SwingConstants.LEFT);
        centralizado.setHorizontalAlignment(SwingConstants.CENTER);
        direita.setHorizontalAlignment(SwingConstants.RIGHT);
        //
        jTabelaUnidades.getColumnModel().getColumn(0).setCellRenderer(centralizado);
    }
