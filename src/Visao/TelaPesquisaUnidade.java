    private void jBtConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtConfirmarActionPerformed
        // TODO add your handling code here:
        flag = 1;
        if (flag == 1) {
            String rezaoSocial = "" + jTabelaUnidades.getValueAt(jTabelaUnidades.getSelectedRow(), 1);
            jPesqUnidade.setText(rezaoSocial);
            String idEmp = "" + jTabelaUnidades.getValueAt(jTabelaUnidades.getSelectedRow(), 0);
            conecta.abrirConexao();
            try {
                conecta.executaSQL("SELECT * FROM UNIDADE_PENAL_EMPRESA "
                        + "WHERE UNIDADE_PENAL_EMPRESA.DescricaoUnidade='" + rezaoSocial + "' "
                        + "AND UNIDADE_PENAL_EMPRESA.IdUnidEmp='" + idEmp + "'");
                conecta.rs.first();
                idUnidade = conecta.rs.getInt("IdUnidEmp");
                jDescricaoUnidade.setText(conecta.rs.getString("DescricaoUnidade"));
                conecta.desconecta();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane, "ERRO na pesquisa por nome" + ex);
            }
            dispose();
        }
    }//GEN-LAST:event_jBtConfirmarActionPerformed

